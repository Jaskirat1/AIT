'''
string functions:-

1.lower()-->all lowercase
2.upper()-->all uppercase
3.title()--> to convert to title
4.capitalize()--->convert starting letter to capital
'''
a = 'MY NAME IS JASKIRAT'
b = 'my name is jaskirat'

l = a.lower()
print(l)
print()

u = a.upper()
print(u)
print()

t= b.title()
print(t)
print()

c = b.capitalize()
print(c)