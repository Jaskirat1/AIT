"""
Comparison operators 

==      |       equal           | x == y
!=      |       notequalto      | x != y
>       |     greaterthan       | x > y
<       |       lessthan        | x < y
>=      | greaterthan or equal  | x >= y
<=      | less than or equal    | x <= y

"""

x= 10
y =20

print(x == y)   #False 
print(x != y)   #True
print(x > y)    #False
print(x < y)    #True
print(x >= y)   #False
print(x <= y)   #True
